    tokens = []
    segment_ids = []
    tokens.append("[CLS]")
    segment_ids.append(0)
    for token in tokens_a:
        tokens.append(token)
        segment_ids.append(0)
    tokens.append("[SEP]")
    segment_ids.append(0)

    if tokens_c:
        for token in tokens_c:
            tokens.append(token)
            segment_ids.append(1)

        # todo 判断
        if tokens[-1] != "[SEP]":
            tokens.append("[SEP]")
